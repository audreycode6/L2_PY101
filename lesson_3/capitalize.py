''''Using the following string, print a string that 
contains the same value, but using all lowercase 
letters except for the first character, which should be capitalized.
'''

munsters_description = "the Munsters are CREEPY and Spooky."
# => 'The munsters are creepy and spooky.'
cap = munsters_description.capitalize()
print(cap)

swap = munsters_description.swapcase()
print(swap)